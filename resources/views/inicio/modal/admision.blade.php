<p style="white-space: pre-wrap;text-align:justify;">El Decanato de Postgrado de la UNESUR oferta anualmente dos veces sus programas de Maestrías, Especializaciones y Doctorados a profesionales de diversas áreas de conocimiento. De acuerdo al artículo 25 del Reglamento de Estudios de Postgrado publicado en Consejo Universitario 022/2007:

Art. 25.- Para ser admitidos en un Programa de Postgrado se requiere haber obtenido un titulo de Licenciado o su equivalente, en universidades venezolanas o extranjeras de reconocido nivel académico o en instituciones de educación superior a nivel universitario, cuyo curriculum contemple estudios de una duración mínima de cuatro (4) años. Los aspirantes deberán además, cumplir otros requisitos de ingreso establecidos para cada programa en particular y en los reglamentos respectivos.

Parágrafo Primero: En el caso de Especializaciones Técnicas, podrán ser admitidos Técnicos Superiores Universitarios, de acuerdo con lo establecido en el artículo 3, Parágrafo Único de la Normativa General de Estudios de Postgrado y el artículo 3 de este Reglamento.

Parágrafo Segundo: Cuando se trate de estudios realizados en universidades extranjeras, el Consejo Académico calificará el nivel de dichos estudios.

Parágrafo Tercero: Cuando se trate de estudiantes egresados de universidades extranjeras, admitidos en programas de Postgrado, la admisión no significa convalidación del titulo de pregrado.</p>



