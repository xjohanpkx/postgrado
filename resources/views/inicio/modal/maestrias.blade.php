                  <p style="white-space: pre-wrap;text-align:justify;">Este Programa de Maestría en Informática es producto de la interpretación de los contextos científicos, tecnológicos y sociales del momento y de las necesidades profesionales locales y nacionales. Su propósito es formar profesionales integrales, competentes en esta área y acordes con los desafíos de la época, quienes recibirán el título de Magister en Informática.

Este Magister tendrá competencia en áreas fundamentales del conocimiento informático para un desempeño eficiente, proactivo, colaborativo y creativo. Reconocido por su espíritu investigativo e innovador en la generación de conocimientos en tecnología informática en beneficio del desarrollo del país. Además, con habilidades socio-emocionales para relacionarse positivamente con los demás y para conocerse y revisarse en función del trabajo en equipo y de la ética, evidencias de su ser y hacer profesional.

La Maestría tendrá gran impacto científico, tecnológico y cultural dentro y fuera de la región, por cuanto ampliará escenarios para los intercambios científicos propios de la sociedad digitalizada y del profesor investigador. El diseño curricular, responde al enfoque por competencias. Por ello el perfil, eje de la formación, se estructuró en competencias genéricas y profesionales. Las profesionales fueron distribuidas en cuatro áreas: Tecnología Básica, Ingeniería de la Información, Telemática e Investigación, las cuales serán atendidas desde cada Unidad Curricular en correspondencia con las competencias genéricas. El plan de estudios, fundamentado en las nuevas concepciones psicológicas, pedagógicas, epistemológicas y didácticas, se organizó en seis (6) trimestres para ser desarrollado mediante 12 Unidades Curriculares con un total de 36 unidades crédito. La enseñanza será presencial y fortalecida con la virtualidad. Además, será estratégica, comprometida con el desarrollo de cada competencia, fomentar la innovación, la investigación tecnológica y garantizará aprendizajes significativos, profundos y transferibles. Ese sistema didáctico se presenta en cada Programa Sinóptico, guía para enseñar colaborativamente y en función de la complejidad de los contenidos: conceptuales, procedimentales y actitudinales, con diversidad de estrategias metodológicas y de evaluación, fomentado así, la calidad de la enseñanza y de las evidencias formativas, sobresale un trabajo de grado aplicable y relevante.</p>
